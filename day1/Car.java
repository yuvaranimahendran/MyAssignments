	int carRegNumber=123456789;
	// upto 9 digits can be stored in int 
	long carRegLongNumber=1234567890123567L;
	// more than 9 digits can be stored in long with L at the end
	float carCeCe=1234567890123888877777777777778888.67677777777777777777777777777767654F;
	// small decimals can be stored in float
	double kiloMeters=3646384684683463.876348743846;
	// long decimals can be stored in double 
	char carLogo='m';
	// only one letter or special char can be stored 
	boolean isCarBeautiful=true;
	// boolean stores only true or false
	String carName="Merc";
	// string stores names and verbs and nouns 
	String carColour="black";
